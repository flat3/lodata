    /**
     * @dataProvider typeProvider
     */
    public function test_types(string $type, array $toUrl, array $toJson)
    {
        foreach (['toUrl', 'toJson'] as $method) {
            foreach ($$method as $fromto) {
                list ($from, $to) = $fromto;

                $this->assertSame(
                    $to, (new $type($from))->$method(),
                    $type.'('.(string) $from.')::'.$method.' -> '.$to
                );
            }
        }
    }
